// Lightbox Elements
const lightbox = document.getElementById('lightbox-modal');
const lightboxImg = document.getElementById('lightbox-img');
const lightboxCounter = document.getElementById('lightbox-counter');
const lightboxTitle = document.getElementById('lightbox-title');
const lightboxCaption = document.getElementById('lightbox-caption');
const lightboxCloseBtn = document.getElementById('lightbox-close-btn');
const lightboxCloseBackdrop = document.getElementById('lightbox-close-backdrop');
const lightboxPrevBtn = document.getElementById('lightbox-prev-btn');
const lightboxNextBtn = document.getElementById('lightbox-next-btn');

function openLightbox(index) {
  if (!lightbox) return;
  currentLightboxIndex = index;
  updateLightbox();
  lightbox.classList.add('active');
  lightbox.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden'; // Lock scrolling
}

function updateLightbox() {
  if (!lightboxImg || !lightboxCounter || !lightboxTitle || !lightboxCaption) return;
  
  const sketch = sketchesData[currentLightboxIndex];
  
  // Fade out image quickly for transition
  lightboxImg.style.opacity = '0';
  
  setTimeout(() => {
    lightboxImg.src = sketch.src;
    lightboxImg.alt = translations[currentLang][sketch.titleKey] || 'Sketch';
    
    lightboxCounter.textContent = `${currentLightboxIndex + 1} / ${sketchesData.length}`;
    
    lightboxTitle.setAttribute('data-i18n', sketch.titleKey);
    lightboxTitle.textContent = translations[currentLang][sketch.titleKey] || '';
    
    lightboxCaption.setAttribute('data-i18n', sketch.captionKey);
    lightboxCaption.textContent = translations[currentLang][sketch.captionKey] || '';
    
    lightboxImg.style.opacity = '1';
  }, 200);
}

function closeLightbox() {
  if (!lightbox) return;
  lightbox.classList.remove('active');
  lightbox.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = ''; // Restore scrolling
}

if (lightboxCloseBtn) lightboxCloseBtn.addEventListener('click', closeLightbox);
if (lightboxCloseBackdrop) lightboxCloseBackdrop.addEventListener('click', closeLightbox);

if (lightboxPrevBtn) {
  lightboxPrevBtn.addEventListener('click', () => {
    currentLightboxIndex = (currentLightboxIndex - 1 + sketchesData.length) % sketchesData.length;
    updateLightbox();
  });
}

if (lightboxNextBtn) {
  lightboxNextBtn.addEventListener('click', () => {
    currentLightboxIndex = (currentLightboxIndex + 1) % sketchesData.length;
    updateLightbox();
  });
}

// Global Key Listeners for Lightbox
window.addEventListener('keydown', (e) => {
  if (lightbox && lightbox.classList.contains('active')) {
    if (e.key === 'Escape') {
      closeLightbox();
    } else if (e.key === 'ArrowRight') {
      currentLightboxIndex = (currentLightboxIndex + 1) % sketchesData.length;
      updateLightbox();
    } else if (e.key === 'ArrowLeft') {
      currentLightboxIndex = (currentLightboxIndex - 1 + sketchesData.length) % sketchesData.length;
      updateLightbox();
    }
  }
});
